# Exemplo: Soma de dois números

# Entrada de dados (entrada sempre é string)
numero1 = input("Digite o primeiro número: ")
numero2 = input("Digite o segundo número: ")

# Conversão de string para inteiro
numero1 = int(numero1)
numero2 = int(numero2)

# Processamento
soma = numero1 + numero2

# Saída de dados
print("A soma é:", soma)
