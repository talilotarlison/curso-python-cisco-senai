# Lista de números
numeros = [1, 2, 3, 4]

# Lista de strings
frutas = ["maçã", "banana", "laranja"]

# Lista com diferentes tipos de dados
lista_diversa = [10, "texto", True]

print(numeros) # Saída: [1, 2, 3, 4]
print(frutas)  # Saída: ['maçã', 'banana', 'laranja']
print(lista_diversa) # Saída: [10, 'texto', True]